package level3;

import java.util.Scanner;
public class Al_10950 {

	public static void main(String[] args) {
				
		int T = 0;
		int A = 0;
		int B = 0;
		
		Scanner sc = new Scanner(System.in);
		T = sc.nextInt();
		
		for(int i= 0; i < T; i++) {
			
			A = sc.nextInt();
			B = sc.nextInt();
			
			System.out.println(A + B);
		} 
		
	} 
}

